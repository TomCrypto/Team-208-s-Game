package network.packet.action;

import network.packet.*;

public class UseAction implements ActionPacket {

	private static final long serialVersionUID = 1L;

	public final float ID;

	public UseAction(float ID) {
		this.ID = ID;
	}

	@Override
	public Type getType() {
		return Type.ACTION;
	}

	@Override
	public ActionType getActionType() {
		return ActionType.USE;
	}



}
