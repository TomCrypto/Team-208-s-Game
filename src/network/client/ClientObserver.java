package network.client;

import network.client.observers.*;

/**
 * This class groups all client observers.
 * 
 * @author Thomas Beneteau (300250968)
 */
public class ClientObserver
{
	public final GeneralObserver general;
	public final LoginObserver login;
	public final GameObserver game;
	public final ChatObserver chat;
	
	public ClientObserver(GeneralObserver general, LoginObserver login, GameObserver game, ChatObserver chat)
	{
		this.general = general;
		this.login = login;
		this.game = game;
		this.chat = chat;
	}
}
